class Card extends Component {
  state = {changingData: data}

  changingBackground = id => {
    const {changingData} = this.state
    const changedData = changingData.filter(each => each.id !== id)
    this.setState({changingData: changedData})
  }

  render() {
    const {changingData} = this.state
    return (
      <div className="mainContainerOne">
        <h1>Eliminating the Emoji</h1>
        <div className="mainContainer">
          {changingData.map(each => (
            <IndividualCard
              each={each}
              changingBackground={this.changingBackground}
              key={each.id}
            />
          ))}
        </div>
      </div>
    )
  }
}

export default Card
